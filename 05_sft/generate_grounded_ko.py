def build_prompt(exemplars, passage, task, term=None,
                 audience_instr="", register_instr=""):
    """Build the few-shot prompt for the base model. If audience_instr and/or
    register_instr are given, prepend them as a single combined header above
    the exemplar block."""
    spec = TASKS[task]
    labels, inputs = spec["labels"], spec["inputs"]
    blocks = ["\n".join(f"{lab}: {ex[lab]}" for lab in labels) for ex in exemplars]
    vals = {"passage": passage, "term": term}
    open_parts = []
    for i, lab in enumerate(labels):
        if i < len(inputs):
            open_parts.append(f"{lab}: {vals[inputs[i]]}")
        else:
            open_parts.append(f"{lab}:")
            break                          # stop after the first generated label
    body = DELIM.join(blocks + ["\n".join(open_parts)])
    instrs = [s for s in (audience_instr, register_instr) if s]
    if instrs:
        body = f"다음 실례들을 참고하여, {' '.join(instrs)}\n\n{body}"
    return body, labels


def _take(completion):
    """Cut completion at the exemplar delimiter or a stray '\\n---'."""
    return completion.split(DELIM)[0].split("\n---")[0].strip()


def parse_completion(completion, task, labels):
    text = _take(completion)
    if not text:
        return None
    if task == "qa":
        m = re.search(rf"(.*?)\n\s*{re.escape(labels[2])}\s*:\s*(.*)", text, re.S)
        return {"question": m.group(1).strip(), "answer": m.group(2).strip()} if m else None
    if task == "reasoned_qa":
        m = re.search(rf"(.*?)\n\s*{re.escape(labels[2])}\s*:\s*(.*)", text, re.S)
        return {"question": m.group(1).strip(), "cot_answer": m.group(2).strip()} if m else None
    if task == "multi_qa":
        pairs = re.findall(r"질문\s*\d*\s*[:.]\s*(.*?)\s*\n\s*답\s*\d*\s*[:.]\s*(.*?)(?=\n\s*질문|\Z)",
                           text, re.S)
        if len(pairs) < 1:
            return None
        return {"qa_pairs": [{"q": q.strip(), "a": a.strip()} for q, a in pairs]}
    return {                                  # single-field tasks
        "summary":         {"summary": text},
        "title":           {"title": text.split("\n")[0].strip()},
        "fact_extraction": {"facts": text},
        "explain_simply":  {"explanation": text},
        "outline":         {"outline": text},
        "definition":      {"definition": text},
    }[task]


def to_chat(passage, parsed, task, term=None,
            audience_phrase="", register_phrase=""):
    """Build the user/assistant chat for SFT. If audience_phrase and/or
    register_phrase are given, prepend them to the user instruction so the
    trained model learns to follow explicit audience/register requests at
    inference."""
    pieces = [p for p in (audience_phrase, register_phrase) if p]
    aud = " ".join(pieces) + " " if pieces else ""
    if task == "qa":
        user = (f"{aud}다음 단락을 읽고 질문에 답하시오.\n\n"
                f"단락: {passage}\n\n질문: {parsed['question']}")
        asst = parsed["answer"]
    elif task == "reasoned_qa":
        user = (f"{aud}다음 단락을 바탕으로 이 질문을 단계별로 분석하여 대답하시오.\n\n"
                f"단락: {passage}\n\n질문: {parsed['question']}")
        asst = parsed["cot_answer"]
    elif task == "multi_qa":
        user = (f"{aud}다음 단락을 바탕으로 질문-대답 {len(parsed['qa_pairs'])}개를 "
                f"만드시오.\n\n단락: {passage}")
        asst = "\n".join(f"질문 {i+1}: {p['q']}\n대답 {i+1}: {p['a']}"
                         for i, p in enumerate(parsed["qa_pairs"]))
    elif task == "summary":
        user, asst = f"{aud}다음 단락을 요약하시오.\n\n{passage}", parsed["summary"]
    elif task == "title":
        user, asst = f"{aud}다음 단락에 어울리는 제목을 제안하시오.\n\n{passage}", parsed["title"]
    elif task == "fact_extraction":
        user, asst = (f"{aud}다음 단락에서 핵심사실을 뽑아 목록으로 만드시오.\n\n{passage}",
                      parsed["facts"])
    elif task == "explain_simply":
        user, asst = (f"{aud}다음 단락을 쉬운 조선어로 설명하시오.\n\n{passage}",
                      parsed["explanation"])
    elif task == "outline":
        user, asst = (f"{aud}다음 단락의 구조적개요를 작성하시오.\n\n{passage}",
                      parsed["outline"])
    elif task == "definition":
        user, asst = (f"{aud}다음 단락을 바탕으로 '{term}'의 정의를 쓰시오.\n\n{passage}",
                      parsed["definition"])
    return {"messages": [{"role": "user", "content": user},
                         {"role": "assistant", "content": asst}]}


def korean_fraction(s):
    letters = [c for c in s if c.isalpha()]
    if not letters:
        return 0.0
    return sum(1 for c in letters if "HANGUL" in unicodedata.name(c, "")) / len(letters)


# Korean is agglutinative: particles (조사) attach to the noun inside one eojeol,
# so 조선/조선은/조선이/조선을 must reduce to one form before overlap is meaningful.
_PARTICLES = sorted([
    "입니다", "이였다", "이며", "이고", "이다",   # fused copula endings (noun+이다)
    "에서는", "으로는", "에게서", "께서는", "이라고", "이라는",
    "에서", "에게", "께서", "으로", "라고", "부터", "까지", "보다", "처럼",
    "만큼", "마다", "조차", "마저", "밖에", "이나", "든지", "이라", "에는",
    "은", "는", "이", "가", "을", "를", "의", "에", "도", "와", "과", "만",
    "로", "야", "께", "라", "나",
], key=len, reverse=True)


def _strip_particle(w):
    for p in _PARTICLES:
        # only strip if a real (>=2-syllable) stem remains, so 2-char nouns
        # like 정의/회의/수도 are NOT truncated to a single syllable.
        if len(w) - len(p) >= 2 and w.endswith(p):
            return w[: -len(p)]
    return w


_KO_STOP = {"그리고", "그러나", "하지만", "또한", "그래서", "따라서", "즉", "및",
            "등", "것", "수", "더", "매우", "아주", "이", "그", "저", "때문",
            "하다", "있다", "없다", "되다", "이다", "한다", "합니다", "입니다",
            "있습니다", "같은", "위해", "통해", "대한", "대해"}


def _content_words(text):
    out = set()
    for w in text.split():
        w = w.strip("。.,?!\"'()[]{}:;…·")
        w = _strip_particle(w)
        if len(w) >= 2 and korean_fraction(w) > 0.5 and w not in _KO_STOP:
            out.add(w)
    return out


def faithful(answer, passage, thresh=0.1):
    src = _content_words(passage)
    if not src:
        return True
    return len(src & _content_words(answer)) / len(src) > thresh


