#!/usr/bin/env python3
"""
Stage 5 (data) — generate passage-grounded NATIVE Korean SFT data from the BASE model.

After Stage 4 the model is a *base* model (completion, not instructions). We use
FEW-SHOT in-context learning: show it native exemplars of the target pattern, append
a real Korean passage, let it continue. The answer is grounded because the passage
sits in the context window.

Supported task types (each has a matching exemplars_<task>.json):
  qa              — single question + answer
  reasoned_qa     — question + multi-step (chain-of-thought) answer
  multi_qa        — 3 question-answer pairs per passage (denser SFT data)
  summary         — abstractive summary
  title           — a single headline
  fact_extraction — bullet-list of key facts
  explain_simply  — passage rewritten in simple Korean
  outline         — hierarchical outline
  definition      — define a given term grounded on the passage

Diversity axes — sweep these to multiply per-passage data and let the SFT model
learn explicit register/audience following:

  --audience  {어린이, 중고급중학교생, 일반, 전문가, mixed}
       audience variation (instruction-driven): prepends an instruction to the
       base-model few-shot header AND prepends a short phrase to the SFT user
       message so the trained model can later follow explicit audience requests.
       'mixed' = no injection (legacy / default behavior).

  --register  {합쇼체, 해요체, 문어체, mixed}
       register variation (instruction-driven, Option A): same mechanism as
       audience — a header instruction steers generation, an SFT-message phrase
       teaches the trained model to follow the request. Combine with --register-
       filter to drop outputs whose dominant register doesn't match (the
       deterministic register check from register_consistency.py).
       For stronger steering, ship register-specific exemplars
       (exemplars_<task>_<register>.json — Option B) and load via --exemplars.
       'mixed' = no injection (legacy / default behavior).

  Run the script in a shell loop to sweep the cross-product
  (audience × register × task) — multiplies effective per-passage data.

Filters: language-ID (Hangul fraction) + faithfulness (content-word overlap, with
Korean particles stripped so 조선은/조선이/조선을 count as the same word; skipped for
`title`) + optional register match (--register-filter). For stricter filtering,
run output through 05_sft/faithfulness_scorer.py and
05_sft/translationese_scorer.py.
"""
import argparse, json, os, re, sys, unicodedata

# Sibling module — used for the optional register post-filter.
sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
from register_consistency import matches_target_register

DELIM = "\n---\n"

# Task spec: ordered exemplar field labels + which input keys fill the leading fields.
# All non-input fields are generated by the model (the first one is the cut point).
TASKS = {
    "qa":              {"labels": ["단락", "질문", "답"],          "inputs": ["passage"]},
    "reasoned_qa":     {"labels": ["단락", "질문", "풀이와 답"],     "inputs": ["passage"]},
    "multi_qa":        {"labels": ["단락", "질문-답"],             "inputs": ["passage"]},
    "summary":         {"labels": ["단락", "요약"],                "inputs": ["passage"]},
    "title":           {"labels": ["단락", "제목"],                "inputs": ["passage"]},
    "fact_extraction": {"labels": ["단락", "핵심사실"],            "inputs": ["passage"]},
    "explain_simply":  {"labels": ["단락", "쉬운 설명"],            "inputs": ["passage"]},
    "outline":         {"labels": ["단락", "개요"],                "inputs": ["passage"]},
    "definition":      {"labels": ["용어", "단락", "정의"],         "inputs": ["term", "passage"]},
}
SKIP_FAITHFUL = {"title"}   # too short for content-word overlap to be meaningful

# Audience axis (Option A — prompt-side instruction injection that stacks on top
# of Option B's register-variant exemplars). Each entry is a (build_prompt header
# instruction, to_chat user-message phrase) pair.
#
# Honest limitation: a base model with weak Korean cannot perfectly follow the
# audience instruction from a single prefix line — the few-shot exemplars don't
# themselves vary by audience. Expect best-effort adherence (~60-80% on a strong
# base, lower on a weak one). Spot-check the output and, if needed, build a
# downstream audience-checker (vocabulary level / sentence length) to drop
# off-audience generations — the analogue of register_consistency.py.
AUDIENCES = {
    "어린이":      ("소학교생도 리해할 수 있게 쉬운 어휘와 짧은 문장으로 작성하시오.",
                    "소학교생도 리해할 수 있게"),
    "중고급중학교생":  ("중·고급중학교생 수준의 어휘와 설명으로 작성하시오.",
                    "중·고급중학교생이 리해할 수 있게"),
    "일반":        ("일반 성인 독자가 리해하기 쉽게 작성하시오.",
                    "일반 성인을 대상으로"),
    "전문가":      ("해당 분야 전문가 수준의 정확한 용어로 작성하시오.",
                    "전문가 수준으로"),
}

# Register axis (Option A — prompt-side instruction injection, no exemplar
# variants needed). Each entry is (build_prompt header instruction, to_chat
# user-message phrase). Skip 해체 — chatbots rarely need 반말 responses.
#
# Honest limitation, same as AUDIENCES: a weak base model only loosely follows
# a single header instruction; the exemplars likely encode a different register.
# Pair with --register-filter to drop outputs whose dominant register doesn't
# match. If hit rate is too low, ship register-specific exemplars (Option B).
REGISTERS = {
    "합쇼체": ("응답을 합쇼체(-습니다/-습니까/-십시오)로 작성하시오.",
              "합쇼체로"),
    "해요체": ("응답을 해요체(-요/-아요/-이에요)로 작성하시오.",
              "해요체로"),
    "문어체": ("응답을 문어체(-다/-이다)의 서술형 산문으로 작성하시오.",
              "문어체로"),
}


