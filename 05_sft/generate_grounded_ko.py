def _selftest():
    # qa
    exs = [{"단락": "대동강은 조선의 주요강이다.",
            "질문": "대동강은 무엇인가?", "답": "대동강은 조선의 주요강이다."}]
    passage = "안학궁은 평양에 있다."
    prompt, labels = build_prompt(exs, passage, "qa")
    assert prompt.rstrip().endswith("질문:")
    parsed = parse_completion(" 안학궁은 어디에 있는가?\n답: 안학궁은 평양에 있다.\n---", "qa", labels)
    assert parsed["question"] == "안학궁은 어디에 있는가?" and "평양" in parsed["answer"]

    # reasoned_qa: multi-step CoT answer
    rq_exs = [{"단락": "...", "질문": "왜?",
               "풀이와 답": "단계 1: ...\n단계 2: ...\n따라서 ..."}]
    p, lbl = build_prompt(rq_exs, passage, "reasoned_qa")
    assert p.rstrip().endswith("질문:")
    parsed = parse_completion(" 왜 그런가?\n풀이와 답: 단계 1: 안학궁은 평양에 있다.\n"
                              "단계 2: 유명한 유적이다.\n따라서 맞다.\n---",
                              "reasoned_qa", lbl)
    assert parsed["question"] == "왜 그런가?"
    assert "단계 1" in parsed["cot_answer"] and "따라서" in parsed["cot_answer"]
    chat = to_chat(passage, parsed, "reasoned_qa")
    assert "단계별로" in chat["messages"][0]["content"]
    assert "단계 1" in chat["messages"][1]["content"]

    # multi_qa parsing
    mqa_exs = [{"단락": "...", "질문-답": "질문 1: a?\n답 1: x\n질문 2: b?\n답 2: y"}]
    p, lbl = build_prompt(mqa_exs, passage, "multi_qa")
    assert p.rstrip().endswith("질문-답:")
    parsed = parse_completion("\n질문 1: 어디?\n답 1: 평양\n질문 2: 무엇?\n답 2: 유적\n---",
                              "multi_qa", lbl)
    assert len(parsed["qa_pairs"]) == 2 and parsed["qa_pairs"][1]["a"] == "유적"

    # title (single line, faithfulness skipped)
    t_exs = [{"단락": "...", "제목": "어떤 제목"}]
    p, lbl = build_prompt(t_exs, passage, "title")
    parsed = parse_completion(" 평양의 자랑\n---", "title", lbl)
    assert parsed["title"] == "평양의 자랑"
    assert "title" in SKIP_FAITHFUL

    # definition includes term
    d_exs = [{"용어": "ㄱ", "단락": "...", "정의": "..."}]
    p, lbl = build_prompt(d_exs, passage, "definition", term="유적")
    assert "용어: 유적" in p and p.rstrip().endswith("정의:")

    # filters and chat shape; particle-aware faithfulness
    assert faithful("안학궁은 평양에 있다.", passage)
    assert not faithful("전혀 관련이 없는 문장이다.", passage)
    # particle stripping: 조선은/조선이 reduce to 조선, so they overlap
    assert faithful("조선의 수도", "조선은 동아시아에 있다.")
    chat = to_chat(passage, {"answer": "평양", "question": "어디?"}, "qa")
    assert chat["messages"][1]["content"] == "평양"

    # ----- audience injection (Option A on top of Option B) -----

    # (a) build_prompt prepends a single instruction header above the exemplar block
    instr, phrase = AUDIENCES["어린이"]
    prompt_a, _ = build_prompt(exs, passage, "qa", audience_instr=instr)
    assert prompt_a.startswith("다음 실례들을 참고하여, "), prompt_a[:80]
    assert "소학교생" in prompt_a.split("\n")[0]
    assert prompt_a.rstrip().endswith("질문:")        # exemplar structure preserved

    # (b) to_chat prepends an audience phrase to the SFT user message
    chat_a = to_chat(passage, {"answer": "평양", "question": "어디?"}, "qa",
                     audience_phrase=phrase)
    assert chat_a["messages"][0]["content"].startswith("소학교생도 리해할 수 있게 ")
    assert "질문: 어디?" in chat_a["messages"][0]["content"]

    # (c) empty audience args preserve legacy behavior exactly
    prompt_b, _ = build_prompt(exs, passage, "qa")
    assert not prompt_b.startswith("다음 실례들을 참고하여")
    chat_b = to_chat(passage, {"answer": "평양", "question": "어디?"}, "qa")
    assert chat_b["messages"][0]["content"].startswith("다음 단락을")

    # (d) each of the 4 explicit audiences produces a distinct header and phrase
    seen_headers, seen_phrases = set(), set()
    for aud in AUDIENCES:
        instr, phrase = AUDIENCES[aud]
        p_aud, _ = build_prompt(exs, passage, "qa", audience_instr=instr)
        c_aud = to_chat(passage, {"answer": "x", "question": "y"}, "qa",
                        audience_phrase=phrase)
        seen_headers.add(p_aud.split("\n")[0])
        seen_phrases.add(c_aud["messages"][0]["content"].split("다음 단락")[0].strip())
    assert len(seen_headers) == 4, seen_headers
    assert len(seen_phrases) == 4, seen_phrases

    # (e) audience phrase reaches non-qa tasks too
